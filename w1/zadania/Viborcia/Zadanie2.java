package w1.zadania.Viborcia;
import static java.lang.Math.*;

public class Zadanie2 {
    public static void main(String[] args) {
        int a=5; System.out.println("a = "+ a);
        int b=10; System.out.println("b = "+ b);
        double c=pow(a,b); System.out.println("c = "+ c);
        double d=sqrt(c); System.out.println("d = "+ d);
        int e=-5; System.out.println("e = "+ e);
        double g=abs(e); System.out.println("g = "+ g);
        double h=10.5001; System.out.println("h = "+ h + " Po zaokragleniu h = " + round(h));
    }
}
