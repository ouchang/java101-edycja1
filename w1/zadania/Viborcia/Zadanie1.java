package w1.zadania.Viborcia;

public class Zadanie1 {
    public static void main(String[] args) {
    int a=1; System.out.println("a = "+ a);
    int b=2; System.out.println("b = "+ b);
    int c=3; System.out.println("c = "+ c);
    int d=4; System.out.println("d = "+ d);
    int ab=a+b;
    int cd=c-d;
    System.out.println("ab = "+ ab);
    System.out.println("cd = "+ cd);
    double e=1.1; System.out.println("e = "+ e);
    double f=2.2; System.out.println("f = "+ f);
    double g=3.3; System.out.println("g = "+ g);
    double h=4.4; System.out.println("h = "+ h);
    double ef=e*f;
    double gh=g/h;
    System.out.println("ef = "+ ef);
    System.out.println("gh = "+ gh);
    System.out.println("ab%b = "+ ab%b);
    char i='A'; System.out.println("i = "+ i);
    char j='a'; System.out.println("j = "+ j);
    System.out.println(i+j + " Jest to suma zmiennych i oraz j, ktora powstala poprzez dodanie odpowiadajacych liczb w tabeli ASCII. " );
    String k="Ala"; System.out.println("k = "+ k);
    String l="ma"; System.out.println("l = "+ l);
    String kl=k+l;
    System.out.println(kl +" Sumujac zmienne k i l zawartosci tych zmiennych sie polaczyly. ");
    boolean m=true; System.out.println("m = "+ m);
    boolean n=false; System.out.println("n = "+ n);
    }
}
